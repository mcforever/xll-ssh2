package com.vo;

public class DegreeVo {
	private String creatYear;
	private int allNumber;
	private int degreeNumber;   //获取学位人数
	private int educationLevelNumber;  //获取学历人数
	
	public String getCreatYear() {
		return creatYear;
	}
	public void setCreatYear(String creatYear) {
		this.creatYear = creatYear;
	}
	public int getAllNumber() {
		return allNumber;
	}
	public void setAllNumber(int allNumber) {
		this.allNumber = allNumber;
	}
	public int getDegreeNumber() {
		return degreeNumber;
	}
	public void setDegreeNumber(int degreeNumber) {
		this.degreeNumber = degreeNumber;
	}
	public int getEducationLevelNumber() {
		return educationLevelNumber;
	}
	public void setEducationLevelNumber(int educationLevelNumber) {
		this.educationLevelNumber = educationLevelNumber;
	}
	
	

}
